<h3>Visiting and Transfer Controller Policy</h3>


<h5>Visiting Controllers</h5>
<ol>
<li><p>
Controllers wishing to apply for visiting status at vZBW shall send a visiting status request through {{ HTML::linkRoute('visit', 'this form') }}. They must also supply an email/letter of recommendation from the ATM at their home ARTCC before their application will be considered; applications without the required letter of recommendation are considered incomplete and will not be acted on.</p></li>

<li><p>
vZBW ARTCC will not provide any training to help with a successful evaluation. The controller is expected to be competent to the level commensurate with their rating. Failure to meet the standards commensurate with the rating the controller already holds may result in termination from the visiting controller roster, and referral back to the controller’s home ARTCC’s ATM and TA, citing the deficiencies noted.</p></li>

<li><p>
At no time will any authorization/certification applied at vZBW supersede controllers' current ratings or certifications at their home ARTCC, and controllers may not proceed further than their rating or certification level at their home ARTCC.</p></li>

</ol>


<h5>Transferring Controllers</h5>
<ol>
<li><p>Controllers wishing to apply for a transfer to the vZBW ARTCC shall apply via the <a href="http://www.vatusa.net/req_transfer.php">VATUSA ARTCC Transfer Request Form</a>. </p></li>

<li><p>Once the OTS evaluation has been passed, and if the controller wishes to do so, the controller may pursue additional certifications by entering the vZBW training program. </p></li>
</ol>


<h5>Applicable to both Visiting and Transferring Controllers</h5>
<ol>
<li>Any controller requesting to visit at or transfer to the Boston ARTCC will be subject to a background/history check, and may be declined if any of the following are true:</p>
<ul>
  <li>He/She has any known current or past disciplinary problems, or</li>
  <li> He/She was previously a member of the vZBW ARTCC and terminated membership due to a conflict, or</li>
  <li> He/She was previously a member of the vZBW ARTCC and terminated membership due to disrespect for vZBW personnel policies, or</li>
  <li> He/She has demonstrated disrespect to any ARTCC and its policies, or</li>
  <li>He/She has been associated with any mistruths or deception regarding application for membership, previous history and/or qualifications, or</li>
  <li>He/She has been associated with any other situation or activity, which in the sole discretion of the ATM and/or DATM, constitute just cause for declining membership into vZBW.</li>
</ul>
</li>
<p><li>The Air Traffic Manager and/or Deputy Air Traffic Manager reserve the right to decline any requests for visiting controller status or transfer requests for just cause. </li></p>
<p>
<li>Upon acceptance of the request to visit or transfer (whichever is applicable), the controller will complete all applicable exams, and will then schedule an over the shoulder competency evaluation with a vZBW mentor/instructor. Once a successful evaluation has taken place, the controller will then be allowed to control at Non-Major positions only.</li></p>
<p>
<li>Upon request to visit or transfer (whichever is applicable), the controller agrees to adhere to all <a href="http://bostonartcc.net/controllers/policies">vZBW Policies</a>, including the <a href="http://bostonartcc.net/controllers/policies/position-restrictions">vZBW Position Restriction Policy</a> and the <a href= "http://bostonartcc.net/controllers/policies/roster-removal">vZBW Roster Removal Policy</a>.</li></p>
<p><li>Controllers who wish to control in Boston airspace on a regular basis but are unable to transfer to Boston, or controllers who do ultimately transfer to Boston may, upon completion of the required written, oral, and practical exams (or a combination thereof), staff positions approved with their certifications as described within the <a href="http://bostonartcc.net/controllers/policies/position-restrictions">vZBW Position Restriction Policy</a>. The local procedure and competency checks shall be administered by the <a href="http://bostonartcc.net/roster?v=staff">vZBW training staff</a>.</li></p>
</ol>



<h5>Initial Certification Flow</h5>
<p><strong>Controllers holding an S1 rating:</strong></p>
<ol>
  <li>SOP       exam</li>
     <li>DEL/GND exam</li>
     <li>S1 OTS       evaluation</li>
</ol>
<p><strong>Controllers holding an S2 rating:</strong></p>
<ol>
  <li>SOP       exam</li>
     <li>Local       Control exam</li>
     <li>S2 OTS       evaluation</li>
</ol>
<p><strong>Controllers holding an S3, C1, or higher rating:</strong> </p>
<ol start="1" type="1">
  <li>SOP       exam</li>
     <li>Local       Control exam</li>
     <li>Approach       Control exam</li>
     <li>S2/S3       OTS evaluation (generally two separate evaluations, but may be given in       one sitting)</li>
</ol>
<p><strong>Notes:</strong></p>
<ol>
  <li>Visiting       S2 controllers gaining their S3 rating at their home ARTCC shall take the ZBW       Approach control exam and must pass an S3 OTS evaluation before being       granted certification at Class C approach positions.</li>

  <li>Visiting controllers and transferees coming from outside of the VATUSA division  shall take the ZBW DEL/GND exam between the ZBW SOP exam and the ZBW Local Control exam, as there are enough pertinent differences in DEL/GND procedures to warrant study and examination.</li>
</ol>
