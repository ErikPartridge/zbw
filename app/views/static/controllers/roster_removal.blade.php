<h3>Roster  Removal Policy</h3>
<h4>NEW  vZBW CONTROLLERS</h4>
<p>All new  controllers, including transfers, may be removed from the roster if any of the  following conditions are met:</p>
<ol start="1" type="1">
  <li>Have       not requested the SOP (vZBW Policies) exam within 30 days of joining vZBW,       OR </li>
  <li>Have       not completed the SOP (vZBW Policies) exam within 60 days of joining vZBW,       OR </li>
  <li>Have       not completed the next exam required of them within 30 days of the       successful completion of the prior exam, OR </li>
  <li>Have       not connected to VATSIM as an observer or have not contacted the ATM and/or DATM within 30 days of  joining vZBW with notice of       the intended absence. </li>
</ol>
<h4>EXISTING  vZBW CONTROLLERS</h4>
<p>All existing  vZBW permanent party (not visiting) controllers will be considered for removal if they have NOT controlled a  contiguous block of 60 mins in a ZBW position within the last 90 days without  notice to the ATM and/or DATM of their intended absence.</p>
<p>Controllers  removed from the roster shall have their vZBW records kept for 6 months after  removal. Should the controller wish to re-join vZBW within 6 months of being  removed, his/her records will normally be restored and he/she will normally retain their current  certifications, at the discretion of the senior staff (ATM, DATM and/or TA). If the request to re-join is made after 6 months of being  removed, then the controller may have to start anew and the New Controller  section may apply. </p>
<h4>VISITING  CONTROLLERS</h4>
<p>All visiting  controllers will be removed from the authorized visitor list if the any of the  following conditions are met:</p>
<ol start="1" type="1">
  <li>Have       not controlled a contiguous block of 60 mins in a ZBW position within the       last 120 days, OR </li>
  <li>Have&nbsp;not       requested ANY of the exams required of them within 30 days of being       approved for visitor status, OR </li>
  <li>Have&nbsp;not       completed ANY of the exams required of them within 60 days of being       approved for visitor status, OR </li>
  <li>Have       not completed the next exam required of them within 30 days of the successful       completion of the prior exam, OR </li>
  <li>Violate       the conditions of any restriction imposed by the vZBW Visiting Controller       Policy </li>
</ol>
<h4>TERMINATION FOR CAUSE</h4>
<p>Any  controller who does any of the following, shall be issued one written notice.  Any subsequent offense will result in the instant removal from the roster. </p>
<ol start="1" type="1">
  <li>Refusal       to adhere to vZBW position restriction policy. </li>
  <li>Insubordination       to a vZBW staff member or VATUSA staff. </li>
  <li>Disrespectful/Offensive       behavior towards another vZBW member. </li>
  <li>Disrespectful/Offensive       behavior towards another VATSIM member. </li>
  <li>Falsification of records and/or training material, including cheating on exams.</li>
</ol>
<h4>TIMELINES</h4>
<p>Extensions  to any deadlines may be made via e-mail to the ATM or DATM and MAY be made on a  case by case basis under unusual circumstances. This policy does not wish to  preclude anyone from participating, but in order to maintain high levels of  service, remaining current with the information and material is vital.</p>
